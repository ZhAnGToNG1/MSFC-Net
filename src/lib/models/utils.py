def flip_lr(x, flip_idx):
  tmp = x.detach().cpu().numpy()[..., ::-1].copy()
  shape = tmp.shape
  for e in flip_idx:
    tmp[:, e[0], ...], tmp[:, e[1], ...] = \
      tmp[:, e[1], ...].copy(), tmp[:, e[0], ...].copy()
  return torch.from_numpy(tmp.reshape(shape)).to(x.device)

def flip_lr_off(x, flip_idx):
  tmp = x.detach().cpu().numpy()[..., ::-1].copy()
  shape = tmp.shape
  tmp = tmp.reshape(tmp.shape[0], 17, 2,
                    tmp.shape[2], tmp.shape[3])
  tmp[:, :, 0, :, :] *= -1
  for e in flip_idx:
    tmp[:, e[0], ...], tmp[:, e[1], ...] = \
      tmp[:, e[1], ...].copy(), tmp[:, e[0], ...].copy()
  return torch.from_numpy(tmp.reshape(shape)).to(x.device)


def init_weights(conv, weights_std=0.01, bias=0):
    nn.init.normal_(conv.weight, std=weights_std)
    nn.init.constant_(conv.bias, val=bias)
    return conv

def conv_1x1(in_channels, out_channels, **kwargs):
    conv = nn.Conv2d(in_channels, out_channels, kernel_size=1, **kwargs)
    conv = init_weights(conv)
    return conv

def conv_3x3(in_channels, out_channels, **kwargs):
    conv = nn.Conv2d(in_channels, out_channels, kernel_size=3, **kwargs)
    conv = init_weights(conv)
    return conv
